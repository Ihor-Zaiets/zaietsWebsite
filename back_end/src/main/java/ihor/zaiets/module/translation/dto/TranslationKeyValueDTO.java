package ihor.zaiets.module.translation.dto;

public class TranslationKeyValueDTO {
    private String key;
    private String value;

    public TranslationKeyValueDTO(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return this.key;
    }

    public String getValue() {
        return this.value;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
